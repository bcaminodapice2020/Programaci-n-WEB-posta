function cambio(a){
    switch (a){
        case 1:
            return "Lunes"
        case 2:
            return "Martes"
        case 3:
            return "Miércoles"
        case 4:
            return "Jueves"
        case 5:
            return "Viernes"
        case 6:
            return "Sábado"
        case 7:
            return "Domingo"
    }
}

console.log(cambio(1))
console.log(cambio(3))
console.log(cambio(7))

